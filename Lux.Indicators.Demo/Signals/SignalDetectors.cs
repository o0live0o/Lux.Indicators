    /// <summary>
    /// 综合信号探测器 - 整合多种来源的信号
    /// </summary>
    public class ComprehensiveSignalDetector
    {
        private readonly List<ISignalDetector> _detectors;

        public ComprehensiveSignalDetector()
        {
            _detectors = new List<ISignalDetector>
            {
                new QuantitativeSignalDetector(),
                new NewsAnalysisSignalDetector(),
                new PaidResourceSignalDetector()
            };
        }

        public ComprehensiveSignalDetector(List<ISignalDetector> detectors)
        {
            _detectors = detectors ?? new List<ISignalDetector>();
        }

        public async Task<List<StockSignal>> DetectSignalsAsync(DateTime fromDate, DateTime toDate)
        {
            var allSignals = new List<StockSignal>();
            
            // 并行获取所有信号源的结果
            var tasks = _detectors.ConvertAll(detector => detector.DetectSignalsAsync(fromDate, toDate));
            var results = await Task.WhenAll(tasks);
            
            foreach (var result in results)
            {
                allSignals.AddRange(result);
            }
            
            // 按置信度排序
            allSignals.Sort((x, y) => y.Confidence.CompareTo(x.Confidence));
            
            return allSignals;
        }
        
        public void AddDetector(ISignalDetector detector)
        {
            _detectors.Add(detector);
        }
        
        public void RemoveDetector(ISignalDetector detector)
        {
            _detectors.Remove(detector);
        }
    }
}